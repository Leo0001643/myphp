            var settings = {
                speeds: 10, //滚动的速度,单位ms
                isPause: true, //滚动后每个消息是否需要暂停，true和false,
                isHover:true, //鼠标悬停是否需要暂停
            };
            var ul = $('#box_ul')[0];
